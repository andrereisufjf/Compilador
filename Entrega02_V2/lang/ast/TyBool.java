package lang.ast;

import lang.visitors.Visitor;

public class TyBool extends BType {
  private int line, column;

  public TyBool(int line, int column) {
    this.line = line;
    this.column = column;
  }

  public int getLine() {
    return line;
  }

  public int getColumn() {
    return column;
  }

  public void accept(Visitor v) { 
		v.visit(this); 
	}
}